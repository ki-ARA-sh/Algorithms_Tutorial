from unittest import TestCase
from _048_fractal.fractal import fractal


class FractalTest(TestCase):
    def test1(self):
        pattern = [['.', '*'], ['.', '.']]
        result = [['.', '*', '*', '*', '*', '*', '*', '*'], ['.', '.', '*', '*', '*', '*', '*', '*'], ['.', '*', '.', '*', '*', '*', '*', '*'], ['.', '.', '.', '.', '*', '*', '*', '*'], ['.', '*', '*', '*', '.', '*', '*', '*'], ['.', '.', '*', '*', '.', '.', '*', '*'], ['.', '*', '.', '*', '.', '*', '.', '*'], ['.', '.', '.', '.', '.', '.', '.', '.']]
        self.assertListEqual(result, fractal(pattern, 2, 3, True))

    def test2(self):
        pattern = [['.', '*', '.'], ['*', '*', '*'], ['.', '*', '.']]
        result = [['.', '*', '.', '*', '*', '*', '.', '*', '.'], ['*', '*', '*', '*', '*', '*', '*', '*', '*'], ['.', '*', '.', '*', '*', '*', '.', '*', '.'], ['*', '*', '*', '*', '*', '*', '*', '*', '*'], ['*', '*', '*', '*', '*', '*', '*', '*', '*'], ['*', '*', '*', '*', '*', '*', '*', '*', '*'], ['.', '*', '.', '*', '*', '*', '.', '*', '.'], ['*', '*', '*', '*', '*', '*', '*', '*', '*'], ['.', '*', '.', '*', '*', '*', '.', '*', '.']]
        self.assertListEqual(result, fractal(pattern, 3, 2, True))